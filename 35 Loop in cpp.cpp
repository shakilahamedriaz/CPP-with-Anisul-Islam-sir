//a loop statement allows us to execute a statement or group of statements multiple times.

// a program to print "Bangladesh " 5 time.
//syntax like C language.// for(initialization,condition,increment/decrement)
#include<bits/stdc++.h>
using namespace std;
int main()
{

    int i;
    for(i=1; i<=10; i++)
    {

        cout<< "Bangladesh : "<<i<<endl;
    }
     cout<< "\n"<< "end of for loop"<<endl;
    return 0;
}
